static  void  Proc2msTask(void)
{
  u8 key;
  static u8 g_kb_buf[16];  // 魹ｽ魹ｽ魹ｽ欞ｺ魹ｽ魹ｽ魹ｽ魹ｽ
  static u8 g_kb_len = 0;  // 魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾖｽ魹ｽ魹ｽ魹ｽ

  if(Get2msFlag())  //魹ｽﾐｶ魹ｽ2ms魹ｽ魹ｽﾖｾﾗｴﾌｬ
  {
    LEDFlicker(250);//LED魹ｽ魹ｽﾋｸ魹ｽ魹ｽ魹ｽ魹ｽ

    /* 魹ｽ魹ｽ魹ｽ魹ｽﾉｨ魹ｽ魹ｽ */
    key = KEYBOARD_Scan();
    if (key != KEY_NONE)
    {
      switch (key)
      {
        case KEY_A:  // A = 魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ
          if (g_kb_len > 0)
          {
            DAC_2ASK_SendData(g_kb_buf, g_kb_len);
            printf("2ASK Send: %d byte(s)¥r¥n", g_kb_len);
            g_kb_len = 0;
          }
          break;

        case KEY_B:  // B = ﾊｮ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ
        case KEY_E:  // E = ﾊｮ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ
          if (g_kb_len < 16)
          {
            g_kb_buf[g_kb_len++] = key;
            printf("Buf[%d] = 0x%02X¥r¥n", g_kb_len - 1, key);
          }
          break;

        case KEY_C:  // C = 魹ｽ魹ｽﾕｻ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ
          g_kb_len = 0;
          printf("Buffer cleared¥r¥n");
          break;

        case KEY_D:  // D = CWﾄ｣ﾊｽ魹ｽﾐｻ魹ｽ
          {
            static u8 cw = 0;
            cw = !cw;
            DAC_2ASK_CW(cw);
            printf("CW: %s (20Hz carrier)¥r¥n", cw ? "ON" : "OFF");
          }
          break;

        case KEY_F:  // F = 魹ｽ魹ｽ魹ｽ魹ｽﾄ｣ﾊｽ魹ｽﾐｻ魹ｽ
          {
            static u8 dbg = 0;
            dbg = !dbg;
            DAC_2ASK_Debug(dbg);
            printf("Debug: %s (0x55 loop)¥r¥n", dbg ? "ON" : "OFF");
          }
          break;

        case KEY_0: case KEY_1: case KEY_2: case KEY_3:
        case KEY_4: case KEY_5: case KEY_6: case KEY_7:
        case KEY_8: case KEY_9:  // 0-9 魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ
          if (g_kb_len < 16)
          {
            g_kb_buf[g_kb_len++] = key;  // key = 0x00‾0x09
            printf("Buf[%d] = 0x%02X¥r¥n", g_kb_len - 1, key);
          }
          break;

        default:
          break;
      }
    }

    Clr2msFlag();   //魹ｽ魹ｽ魹ｽ2ms魹ｽ魹ｽﾖｾ
  }
}

