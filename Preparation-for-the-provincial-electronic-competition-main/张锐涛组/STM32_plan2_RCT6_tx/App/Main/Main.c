/*********************************************************************************************************
* �������ƣ�Proc2msTask
* �������ܣ�2ms�������� (LED��˸ + ���̼���)
* ���������void
* ���������void
* �� �� ֵ��void
* �������ڣ�2018��01��01��
* ע    �⣺
*********************************************************************************************************/
static  void  Proc2msTask(void)
{
  u8 key;
  static u8 g_kb_buf[16];  // 输入缓冲区
  static u8 g_kb_len = 0;  // 当前缓冲字节数
  static u8 g_cw_on  = 0;  // CW模式状态追踪
  static u8 g_dbg_on = 0;  // Debug模式状态追踪

  if(Get2msFlag())  //�ж�2ms��־״̬
  {
    LEDFlicker(250);//LED��˸����

    /* ����ɨ�� */
    key = KEYBOARD_Scan();
    if (key != KEY_NONE)
    {
      switch (key)
      {
        case KEY_A:  // A = 发送 (自动BCD压缩)
          if (g_kb_len > 0)
          {
            u8 bcd_buf[8], bcd_len = 0;
            u8 i;
            u8 all_digits = 1;
            for (i = 0; i < g_kb_len; i++) {
              if (g_kb_buf[i] > 0x09) { all_digits = 0; break; }
            }
            if (all_digits && g_kb_len >= 2) {
              for (i = 0; i < g_kb_len; i += 2) {
                if (i + 1 < g_kb_len)
                  bcd_buf[bcd_len++] = (g_kb_buf[i+1] << 4) | g_kb_buf[i];
                else
                  bcd_buf[bcd_len++] = g_kb_buf[i];
              }
              DAC_2ASK_SendDataNoCS(bcd_buf, bcd_len);  /* 无校验快速帧 */
              printf("2ASK NoCS: %d->%d byte(s)\r\n", g_kb_len, bcd_len);
            } else {
              DAC_2ASK_SendData(g_kb_buf, g_kb_len);
              printf("2ASK Send: %d byte(s)\r\n", g_kb_len);
            }
            g_kb_len = 0;
          }
          break;

        case KEY_B:  // B = ʮ������
        case KEY_E:  // E = ʮ������
          if (g_kb_len < 16)
          {
            g_kb_buf[g_kb_len++] = key;
            printf("Buf[%d] = 0x%02X\r\n", g_kb_len - 1, key);
          }
          break;

        case KEY_C:  // C = ��ջ�����
          g_kb_len = 0;
          printf("Buffer cleared\r\n");
          break;

        case KEY_D:  // D = CW模式切换 (与Debug互斥)
          g_cw_on = !g_cw_on;
          if (g_cw_on) {
              g_dbg_on = 0;
              DAC_2ASK_Debug(0);
          }
          DAC_2ASK_CW(g_cw_on);
          printf("CW: %s\r\n", g_cw_on ? "ON" : "OFF");
          break;

        case KEY_F:  // F = 调试模式切换 (与CW互斥)
          g_dbg_on = !g_dbg_on;
          if (g_dbg_on) {
              g_cw_on = 0;
              DAC_2ASK_CW(0);
          }
          DAC_2ASK_Debug(g_dbg_on);
          printf("Debug: %s\r\n", g_dbg_on ? "ON" : "OFF");
          break;

        case KEY_0: case KEY_1: case KEY_2: case KEY_3:
        case KEY_4: case KEY_5: case KEY_6: case KEY_7:
        case KEY_8: case KEY_9:  // 0-9 ��������
          if (g_kb_len < 16)
          {
            g_kb_buf[g_kb_len++] = key;  // key = 0x00~0x09
            printf("Buf[%d] = 0x%02X\r\n", g_kb_len - 1, key);
          }
          break;

        default:
          break;
      }
    }

    /* PC0按键: 发送数字1用于测试帧结构 */
    if (PC0_BTN_Scan())
    {
      u8 data = 0x01;
      DAC_2ASK_SendData(&data, 1);
      printf("PC0: Send 0x01\r\n");
    }

    Clr2msFlag();   //���2ms��־
  }
}

