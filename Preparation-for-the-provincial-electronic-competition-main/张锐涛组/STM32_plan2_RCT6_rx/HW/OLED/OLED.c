void OLED_ShowString(u8 x, u8 y, const char *str)
{
    u8 c, i;
    while (*str) {
        c = *str - ' ';
        if (c > 90) c = 0;
        for (i = 0; i < 6 && x < 128; i++, x++)
            g_oled_buf[x][y] = F6x8[c][i];
        str++;
    }
}

static const u8 hex_chars[] = "0123456789ABCDEF";

void OLED_ShowHex(u8 x, u8 y, u8 *data, u8 len)
{
    u8 i;
    char buf[3];
    for (i = 0; i < len; i++) {
        buf[0] = hex_chars[data[i] >> 4];
        buf[1] = hex_chars[data[i] & 0x0F];
        buf[2] = '\0';
        OLED_ShowString(x + i * 12, y, buf);
    }
}

void OLED_ShowNum(u8 x, u8 y, u32 num, u8 digits)
{
    char buf[12];
    u8 i = 0;
    u8 start;
    if (num == 0) {
        buf[i++] = '0';
    } else {
        while (num && i < 10) {
            buf[i++] = (num % 10) + '0';
            num /= 10;
        }
    }
    while (i < digits) buf[i++] = '0';
    start = 0;
    while (start < i / 2) {
        char t = buf[start];
        buf[start] = buf[i - 1 - start];
        buf[i - 1 - start] = t;
        start++;
    }
    buf[i] = '\0';
    OLED_ShowString(x, y, buf);
}

void OLED_Refresh(void)
{
    u8 x, y;
    for (y = 0; y < 8; y++) {
        oled_set_pos(0, y);
        OLED_DC_H();
        OLED_CS_L();
        for (x = 0; x < 128; x++)
            oled_spi_write(g_oled_buf[x][y]);
        OLED_CS_H();
    }
}
